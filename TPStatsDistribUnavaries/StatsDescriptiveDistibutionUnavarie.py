def visualiser_distributions(df):
    """
    Crée des boxplots pour comparer les distributions
    """
    fig, axes = plt.subplots(1, 2, figsize=(14, 5))

    # Préparation des données
    data_A = df['Pays_A'].iloc[:-1].values
    data_B = df['Pays_B'].iloc[:-1].values

    # Boxplot
    axes[0].boxplot([data_A, data_B], labels=['Pays A', 'Pays B'],
                    patch_artist=True,
                    boxprops=dict(facecolor='lightblue'),
                    medianprops=dict(color='red', linewidth=2))
    axes[0].set_ylabel('Exportations (Mtep)')
    axes[0].set_title('Boxplot des exportations par type')
    axes[0].grid(True, alpha=0.3, axis='y')

    # Histogrammes superposés
    axes[1].hist(data_A, bins=4, alpha=0.7, label='Pays A',
                 color='steelblue', edgecolor='black')
    axes[1].hist(data_B, bins=4, alpha=0.7, label='Pays B',
                 color='coral', edgecolor='black')
    axes[1].set_xlabel('Exportations (Mtep)')
    axes[1].set_ylabel('Fréquence')
    axes[1].set_title('Distribution des exportations')
    axes[1].legend()
    axes[1].grid(True, alpha=0.3, axis='y')

    plt.suptitle('Analyse des distributions', fontsize=14, fontweight='bold')
    plt.tight_layout()
    plt.show()

visualiser_distributions(df)

