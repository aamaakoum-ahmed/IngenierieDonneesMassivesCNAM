def diagramme_batons_empiles(df):
    """
    Diagramme en bâtons empilés montrant la contribution de chaque type
    """
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(15, 6))

    # Préparation des données
    types = df['Type_energie'].iloc[:-1]
    A = df['Pays_A'].iloc[:-1].values
    B = df['Pays_B'].iloc[:-1].values

    # Graphique 1 : Valeurs absolues empilées
    ax1.bar(types, A, label='Pays A', color='steelblue', edgecolor='black')
    ax1.bar(types, B, bottom=A, label='Pays B', color='coral', edgecolor='black')
    ax1.set_ylabel('Exportations totales (Mtep)', fontsize=12)
    ax1.set_title('Exportations totales par type', fontsize=12, fontweight='bold')
    ax1.legend()
    ax1.grid(True, alpha=0.3, axis='y')
    ax1.tick_params(axis='x', rotation=45)

    # Graphique 2 : Pourcentages
    total_A = A.sum()
    total_B = B.sum()

    pct_A = (A / total_A * 100).round(1)
    pct_B = (B / total_B * 100).round(1)

    x2 = np.arange(len(types))
    width = 0.35

    ax2.bar(x2 - width/2, pct_A, width, label='Pays A',
            color='steelblue', edgecolor='black')
    ax2.bar(x2 + width/2, pct_B, width, label='Pays B',
            color='coral', edgecolor='black')
    ax2.set_ylabel('Pourcentage (%)', fontsize=12)
    ax2.set_title('Structure en pourcentage', fontsize=12, fontweight='bold')
    ax2.set_xticks(x2)
    ax2.set_xticklabels(types, rotation=45, ha='right')
    ax2.legend()
    ax2.grid(True, alpha=0.3, axis='y')

    plt.suptitle('Comparaison Pays A vs Pays B', fontsize=14, fontweight='bold')
    plt.tight_layout()
    plt.show()

diagramme_batons_empiles(df)

