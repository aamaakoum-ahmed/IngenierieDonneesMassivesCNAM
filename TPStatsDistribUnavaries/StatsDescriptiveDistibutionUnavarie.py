def analyse_statistique(df):
    """
    Calcule les principales statistiques pour chaque pays
    """
    print("=" * 70)
    print("ANALYSE STATISTIQUE DES EXPORTATIONS")
    print("=" * 70)

    for pays in ['Pays_A', 'Pays_B']:
        valeurs = df[pays].iloc[:-1].values
        total = df[pays].iloc[-1]

        print(f"\n--- {pays.replace('_', ' ')} ---")
        print(f"Total exportations : {total} Mtep")
        print(f"Moyenne par type : {valeurs.mean():.1f} Mtep")
        print(f"Écart-type : {valeurs.std():.1f} Mtep")
        print(f"Minimum : {valeurs.min()} Mtep ({df['Type_energie'].iloc[valeurs.argmin()]})")
        print(f"Maximum : {valeurs.max()} Mtep ({df['Type_energie'].iloc[valeurs.argmax()]})")
        print(f"Étendue : {valeurs.max() - valeurs.min()} Mtep")

        # Coefficient de variation (dispersion relative)
        cv = (valeurs.std() / valeurs.mean()) * 100
        print(f"Coefficient de variation : {cv:.1f}%")

        # Indice de concentration de Hirschman-Herfindahl
        parts = (valeurs / total * 100) / 100  # proportions
        hhi = (parts ** 2).sum() * 10000
        print(f"Indice HHI (concentration) : {hhi:.0f}")
        if hhi < 1500:
            print("  → Structure diversifiée")
        elif hhi < 2500:
            print("  → Concentration modérée")
        else:
            print("  → Forte concentration")

    # Comparaison des structures
    print("\n" + "-" * 40)
    print("COMPARAISON DES STRUCTURES")
    print("-" * 40)

    # Test du χ² d'indépendance (pour voir si les structures diffèrent)
    from scipy.stats import chi2_contingency

    tableau_contingence = np.array([
        df['Pays_A'].iloc[:-1].values,
        df['Pays_B'].iloc[:-1].values
    ])

    chi2, p_value, dof, expected = chi2_contingency(tableau_contingence)

    print(f"Test du χ² d'indépendance :")
    print(f"  χ² = {chi2:.2f}")
    print(f"  ddl = {dof}")
    print(f"  p-value = {p_value:.4f}")

    if p_value < 0.05:
        print("  → Les structures sont significativement différentes")
    else:
        print("  → Pas de différence significative détectée")

    return chi2, p_value

# Exécution
chi2, p_value = analyse_statistique(df)

