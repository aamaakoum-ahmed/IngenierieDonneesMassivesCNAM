def calculer_quartiles(df):
    """
    Calcule Q1, Q3 et l'écart interquartile
    """
    total = df['effectif'].sum()

    # Q1 : rang = total/4
    rang_q1 = total / 4

    # Q3 : rang = 3*total/4
    rang_q3 = 3 * total / 4

    # Recherche des classes
    def trouver_valeur_quantile(rang):
        effectif_cum = 0
        for idx, row in df.iterrows():
            if effectif_cum + row['effectif'] >= rang:
                quantile = (row['borne_inf'] +
                           (rang - effectif_cum) / row['effectif'] *
                           row['amplitude'])
                return quantile, row, effectif_cum
            effectif_cum += row['effectif']

    q1, classe_q1, avant_q1 = trouver_valeur_quantile(rang_q1)
    q3, classe_q3, avant_q3 = trouver_valeur_quantile(rang_q3)

    ecart_interquartile = q3 - q1

    print("=" * 60)
    print("QUARTILES")
    print("=" * 60)
    print(f"Q1 (25%) : {q1:.2f} milliers de litres")
    print(f"   Rang : {rang_q1:.0f}e exploitation")
    print(f"   Classe : [{classe_q1['borne_inf']}; {classe_q1['borne_sup']}[")
    print(f"\nQ3 (75%) : {q3:.2f} milliers de litres")
    print(f"   Rang : {rang_q3:.0f}e exploitation")
    print(f"   Classe : [{classe_q3['borne_inf']}; {classe_q3['borne_sup']}[")
    print(f"\nÉcart interquartile : {ecart_interquartile:.2f} milliers de litres")
    print(f"                     soit {ecart_interquartile*1000:.0f} litres")

    return q1, q3, ecart_interquartile

q1, q3, eiq = calculer_quartiles(df)

