def calculer_mediane(df):
    """
    Calcule la médiane par interpolation linéaire dans la classe médiane
    """
    total = df['effectif'].sum()
    rang_mediane = total / 2  # 172e exploitation

    # Recherche de la classe médiane
    effectif_cum = 0
    for idx, row in df.iterrows():
        if effectif_cum + row['effectif'] >= rang_mediane:
            classe_mediane = row
            effectif_avant = effectif_cum
            break
        effectif_cum += row['effectif']

    # Interpolation linéaire
    # Med = borne_inf + (rang_mediane - effectif_avant) / effectif_classe * amplitude
    mediane = (classe_mediane['borne_inf'] +
               (rang_mediane - effectif_avant) / classe_mediane['effectif'] *
               classe_mediane['amplitude'])

    print("=" * 60)
    print("MÉDIANE")
    print("=" * 60)
    print(f"Rang de la médiane : {rang_mediane:.0f}e exploitation")
    print(f"Classe médiane : [{classe_mediane['borne_inf']}; {classe_mediane['borne_sup']}[")
    print(f"Effectif avant la classe : {effectif_avant}")
    print(f"Effectif de la classe : {classe_mediane['effectif']}")
    print(f"Médiane = {mediane:.2f} milliers de litres")
    print(f"          soit {mediane*1000:.0f} litres")

    return mediane, classe_mediane

mediane, classe_mediane = calculer_mediane(df)

