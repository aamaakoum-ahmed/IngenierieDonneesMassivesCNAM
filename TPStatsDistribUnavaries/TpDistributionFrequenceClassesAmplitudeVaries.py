# VARIANCE ET ÉCART-TYPE
# ============================================================

def calculer_variance(df, moyenne):
    """
    Calcule la variance et l'écart-type (formule pondérée)
    """
    total = df['effectif'].sum()

    # Somme des (centre - moyenne)² * effectif
    somme_carres_ecarts = ((df['centre'] - moyenne)**2 * df['effectif']).sum()

    # Variance (population, pas échantillon)
    variance = somme_carres_ecarts / total

    # Écart-type
    ecart_type = np.sqrt(variance)

    # Coefficient de variation
    cv = (ecart_type / moyenne) * 100

    print("=" * 60)
    print("VARIANCE ET ÉCART-TYPE")
    print("=" * 60)
    print(f"Variance : {variance:.2f} (milliers de litres)²")
    print(f"Écart-type : {ecart_type:.2f} milliers de litres")
    print(f"            soit {ecart_type*1000:.0f} litres")
    print(f"Coefficient de variation : {cv:.1f}%")

    if cv < 15:
        print("→ Distribution peu dispersée (homogène)")
    elif cv < 30:
        print("→ Dispersion modérée")
    else:
        print("→ Distribution très dispersée (hétérogène)")

    return variance, ecart_type

variance, ecart_type = calculer_variance(df, moyenne)

