# interprétation détaillée

def interpreter_axes(correlations, axe, seuil=0.5):

    corr_axe = correlations.iloc[:, axe]
    print(f"\n----------- INTERPRÉTATION DE L'AXE {axe+1} ------------")
    print(f"Inertie expliquée : {inertie_expliquee[axe]*100:.1f}%")

    # Corrélations positives
    pos = corr_axe[corr_axe > seuil].sort_values(ascending=False)
    if not pos.empty:
        print(f"\nVariables corrélées POSITIVEMENT (r > {seuil}):")
        for var, val in pos.items():
            print(f"  {var}: {val:.3f}")

    # Corrélations négatives
    neg = corr_axe[corr_axe < -seuil].sort_values()
    if not neg.empty:
        print(f"\nVariables corrélées NÉGATIVEMENT (r < -{seuil}):")
        for var, val in neg.items():
            print(f"  {var}: {val:.3f}")

    # Synthèse
    return corr_axe

