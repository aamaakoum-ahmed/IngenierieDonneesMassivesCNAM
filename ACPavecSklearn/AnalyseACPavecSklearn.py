def plot_individus(df_pca, axe_x=0, axe_y=1):

    plt.figure(figsize=(12, 8))
    # Tracé des points
    scatter = plt.scatter(df_pca[f'PC{axe_x+1}'], df_pca[f'PC{axe_y+1}'],
                         alpha=0.7, s=80, c='steelblue', edgecolor='black')

    # Ajout des étiquettes
    for i, nom in enumerate(df_pca['NOM_COURT']):
        plt.annotate(nom,
                    (df_pca.iloc[i, axe_x], df_pca.iloc[i, axe_y]),
                    xytext=(5, 5), textcoords='offset points',
                    fontsize=8, alpha=0.8)

    # Axes
    plt.axhline(y=0, color='gray', linestyle='-', linewidth=0.5, alpha=0.5)
    plt.axvline(x=0, color='gray', linestyle='-', linewidth=0.5, alpha=0.5)

    plt.xlabel(f'Axe {axe_x+1} ({inertie_expliquee[axe_x]*100:.1f}%)', fontsize=12)
    plt.ylabel(f'Axe {axe_y+1} ({inertie_expliquee[axe_y]*100:.1f}%)', fontsize=12)
    plt.title('Projection des individus', fontsize=14, fontweight='bold')
    plt.grid(True, alpha=0.3)

    # Ajout des ellipses de concentration
    from matplotlib.patches import Ellipse
    if len(df_pca) > 5:
        # Ellipse à 95% de confiance
        cov = np.cov(df_pca[[f'PC{axe_x+1}', f'PC{axe_y+1}']].T)
        lambda_, v = np.linalg.eig(cov)
        lambda_ = np.sqrt(lambda_)
        ell = Ellipse(xy=(df_pca[f'PC{axe_x+1}'].mean(), df_pca[f'PC{axe_y+1}'].mean()),
                     width=lambda_[0]*2*2, height=lambda_[1]*2*2,
                     angle=np.rad2deg(np.arccos(v[0, 0])),
                     facecolor='none', edgecolor='red', linewidth=2, alpha=0.5)
        plt.gca().add_patch(ell)

    plt.tight_layout()
    plt.show()

# Tracé des individus
plot_individus(df_pca, 0, 1)