#tracé du cercle de corrélation
def plot_cercle_correlations(correlations, axe_x=0, axe_y=1, titre="Cercle des corrélations"):

    fig, ax = plt.subplots(figsize=(10, 10))
    cercle = plt.Circle((0, 0), 1, fill=False, edgecolor='black', linewidth=1.5, linestyle='-', alpha=0.7)
    ax.add_patch(cercle)

    ax.axhline(y=0, color='gray', linestyle='-', linewidth=0.5, alpha=0.5)
    ax.axvline(x=0, color='gray', linestyle='-', linewidth=0.5, alpha=0.5)

    for i, var in enumerate(correlations.index):
        x = correlations.iloc[i, axe_x]
        y = correlations.iloc[i, axe_y]

        distance = np.sqrt(x**2 + y**2)
        if distance > 0.5:
            couleur = 'darkred'
            alpha = 1.0
            linewidth = 1.5
        elif distance > 0.3:
            couleur = 'coral'
            alpha = 0.8
            linewidth = 1.2
        else:
            couleur = 'lightcoral'
            alpha = 0.6
            linewidth = 1.0

        # Tracé de la flèche
        ax.arrow(0, 0, x, y,
                 head_width=0.03, head_length=0.03,
                 fc=couleur, ec=couleur, alpha=alpha, linewidth=linewidth)

        ax.annotate(var, (x, y),
                   xytext=(x + 0.02 * np.sign(x), y + 0.02 * np.sign(y)),
                   fontsize=10, fontweight='bold',
                   color='darkblue', alpha=1.0,
                   bbox=dict(boxstyle="round,pad=0.2", facecolor="white", alpha=0.8, edgecolor="none"))

    ax.set_xlim(-1.1, 1.1)
    ax.set_ylim(-1.1, 1.1)
    ax.set_aspect('equal')
    ax.set_xlabel(f'Axe {axe_x+1} ({inertie_expliquee[axe_x]*100:.1f}%)', fontsize=12, fontweight='bold')
    ax.set_ylabel(f'Axe {axe_y+1} ({inertie_expliquee[axe_y]*100:.1f}%)', fontsize=12, fontweight='bold')
    ax.set_title(titre, fontsize=14, fontweight='bold')

    # Grille
    ax.grid(True, alpha=0.3, linestyle='--')

    for r in [0.5, 0.7]:
        cercle_inter = plt.Circle((0, 0), r, fill=False, edgecolor='gray', linewidth=0.5, linestyle='--', alpha=0.3)
        ax.add_patch(cercle_inter)

    plt.tight_layout()
    plt.show()

# Tracé du cercle pour les deux premiers axes
