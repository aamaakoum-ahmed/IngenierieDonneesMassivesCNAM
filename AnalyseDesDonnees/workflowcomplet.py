def pipeline_data_mining(df, target, tache='regression', test_size=0.3):
    """
    Pipeline complet de data mining
    """
    print("=" * 70)
    print("PIPELINE DE DATA MINING")
    print("=" * 70)

    # 1. Séparation des variables
    X = df.drop(columns=[target] + ['NOM_COURT'] if 'NOM_COURT' in df.columns else [target])
    y = df[target]

    # 2. Gestion des valeurs manquantes (si besoin)
    if X.isnull().sum().sum() > 0:
        print("\nTraitement des valeurs manquantes...")
        X = X.fillna(X.median())

    # 3. Encodage des variables catégorielles (si besoin)
    cat_cols = X.select_dtypes(include=['object']).columns
    if len(cat_cols) > 0:
        from sklearn.preprocessing import OneHotEncoder
        from sklearn.compose import ColumnTransformer
        preprocessor = ColumnTransformer(
            transformers=[('cat', OneHotEncoder(drop='first'), cat_cols)],
            remainder='passthrough'
        )
        X = preprocessor.fit_transform(X)

    # 4. Standardisation
    scaler = StandardScaler()
    X_scaled = scaler.fit_transform(X)

    # 5. Division train/test
    X_train, X_test, y_train, y_test = train_test_split(
        X_scaled, y, test_size=test_size, random_state=42
    )

    # 6. Benchmark des modèles
    print(f"\nTaille train: {len(X_train)}, Taille test: {len(X_test)}")
    results = benchmark_modeles(X_train, X_test, y_train, y_test, tache)

    # 7. Sélection du meilleur modèle
    if tache == 'regression':
        best_model_name = results['R²'].idxmax()
    else:
        best_model_name = results['Accuracy'].idxmax()

    print(f"\nMeilleur modèle: {best_model_name}")

    return X_train, X_test, y_train, y_test, results

