#approche professionnelle comparaison des modèles
def benchmark_modeles(X_train, X_test, y_train, y_test, tache='regression'):

    from sklearn.linear_model import LinearRegression, LogisticRegression
    from sklearn.tree import DecisionTreeRegressor, DecisionTreeClassifier
    from sklearn.ensemble import RandomForestRegressor, RandomForestClassifier
    from sklearn.svm import SVR, SVC
    from sklearn.metrics import mean_squared_error, r2_score, accuracy_score, f1_score, roc_auc_score

    if tache == 'regression':
        modeles = {
            'Régression Linéaire': LinearRegression(),
            'Arbre de décision': DecisionTreeRegressor(max_depth=5, random_state=42),
            'Random Forest': RandomForestRegressor(n_estimators=100, random_state=42, n_jobs=-1),
            'XGBoost': xgb.XGBRegressor(n_estimators=100, random_state=42),
            'SVR': SVR(kernel='rbf')
        }
        metriques = ['R²', 'RMSE', 'MAE']
    else:
        modeles = {
            'Régression Logistique': LogisticRegression(random_state=42),
            'Arbre de décision': DecisionTreeClassifier(max_depth=5, random_state=42),
            'Random Forest': RandomForestClassifier(n_estimators=100, random_state=42, n_jobs=-1),
            'XGBoost': xgb.XGBClassifier(n_estimators=100, random_state=42),
            'SVM': SVC(kernel='rbf', probability=True, random_state=42)
        }
        metriques = ['Accuracy', 'F1-score', 'AUC']

    results = {}

    for nom, modele in modeles.items():
        # Entraînement
        modele.fit(X_train, y_train)

        # Prédictions
        y_pred = modele.predict(X_test)

        if tache == 'regression':
            results[nom] = {
                'R²': r2_score(y_test, y_pred),
                'RMSE': np.sqrt(mean_squared_error(y_test, y_pred)),
                'MAE': mean_absolute_error(y_test, y_pred)
            }
        else:
            # Pour l'AUC, besoin des probabilités
            if hasattr(modele, 'predict_proba'):
                y_proba = modele.predict_proba(X_test)[:, 1]
                auc_score = roc_auc_score(y_test, y_proba)
            else:
                auc_score = np.nan

            results[nom] = {
                'Accuracy': accuracy_score(y_test, y_pred),
                'F1-score': f1_score(y_test, y_pred, average='weighted'),
                'AUC': auc_score
            }

    # Affichage des résultats
    df_results = pd.DataFrame(results).T
    print(f"\n=== BENCHMARK DES MODÈLES - {tache.upper()} ===")
    print(df_results.round(3))

    # Visualisation
